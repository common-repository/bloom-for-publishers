    <h3>About Geotagging</h3>
    <p><em>This geotagging tool is provided by Bloom Labs in the Bloom for Publishers plugin.</em></p>
    <p>Add locations that are discussed in this story to make it accessible to nearby readers in location-based experiences in websites, apps, and newsletters. You can create and manage these in your Bloom account at <a href="https://www.bloom.li" target="_blank" rel="noopener">www.bloom.li</a>.</p>
    <h4>Best Practices</h4>
    <ul>
        <li>Only geotag stories that are written about a geographic place or region.</li>
        <li>Only add locations that are discussed in the story. Limited to 30 locations total.</li>
        <li>You can add locations that are hyperlocal, such as a street address or park, and regional, such as a city or county.</li>
        <li>If the geotagging search doesn't seem to be working, visit the plugin's General settings to check for "invalid" keys.</li>
    </ul>
